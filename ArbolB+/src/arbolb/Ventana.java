    private void txfBuscarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txfBuscarKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (!txfBuscar.getText().equals("")) {

                arbolito.ayudanteBuscarr(Integer.parseInt(txfBuscar.getText()));
                txfBuscar.setText(""); //limpia el area de texto para ingreso de otro número
                txfBuscar.requestFocus();
            } else {
                JOptionPane.showMessageDialog(null, "El area de texto está vacío");
                txfBuscar.requestFocus();
            }
        }
    }//GEN-LAST:event_txfBuscarKeyPressed

