    private void btnOrdenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrdenActionPerformed
        // Manda como parametro el orden del arbol que se pide
        if (!txfOrden.getText().equals("")) {
            ordenArbol = Integer.parseInt(txfOrden.getText());
            arbolito = new Arbol(ordenArbol);
            nodo = new Nodo(ordenArbol);
            lblOrden.setText("Orden del árbol actual: " + ordenArbol);
            lblOrden.setVisible(true);
            txfOrden.setText(""); //limipia el area de texto
            txfOrden.requestFocus();
        } else {
            JOptionPane.showMessageDialog(null, "El area de texto está vacío");
            txfOrden.requestFocus();
        }
    }//GEN-LAST:event_btnOrdenActionPerformed

